package com.pwing.guilds.alliance;

/**
 * Represents the different roles a player can have in an alliance
 */
public enum AllianceRole {
    /**
     * Leader of the alliance with full permissions
     */
    LEADER,
    
    /**
     * Officer role with elevated permissions
     */
    OFFICER,
    
    /**
     * Basic member of the alliance
     */
    MEMBER
}
